    void FillTableHoaDon() {
        if (cbbKH.getSelectedItem().toString() != null) {
            try {
                DefaultTableModel model = (DefaultTableModel) tbl_HoaDon.getModel();
                model.setRowCount(0);
                String MaSV = cbbKH.getSelectedItem().toString();
                List<HoaDon> list = hddao.selectByMaSV(MaSV);
                for (HoaDon hd : list) {
                    int slmh = 0;
                    float money = 0;
                    List<MonHoc> mh = mhDao.selectByHocKyandmoney(hd.getHocKy(), hd.getMaChuyenNganh());
                    for (MonHoc monHoc : mh) {
                        slmh++;
                        money += monHoc.getHocPhi();
                    }
                    Object[] row = {
                      hd.getMaHoaDon(),
                        hd.getMaSinhVien(),
                     hd.getMaChuyenNganh(),
                          hd.getHocKy(),
                             Math.round(money),
                                   slmh,
//                     XDate.toString(hd.getNgayTaoHoaDon(), "dd-MM-yyyy"),
                        hd.isTinhTrang() == true ? "Đã Thanh Toán" : "Chưa Thanh Toán"
                    };
                    model.addRow(row);
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        } else {
            DefaultTableModel model = (DefaultTableModel) tbl_HoaDon.getModel();
            model.setRowCount(0);
        }
    }

    void Filltable(String MaHK,String MaCN) {
        DefaultTableModel model = (DefaultTableModel) tblMonHoc.getModel();
        model.setRowCount(0);
        try {
            List<MonHoc> list = mhDao.selectByHocKy(MaHK,MaCN);
            for (MonHoc mh : list) {
                Object[] row = {
                    mh.getMaMonHoc(),
                    mh.getTenMonHoc(),
                    mh.getHocPhi(),
                    mh.getHocKy(),
                    mh.getMaChuyenNganh()
                };
                model.addRow(row);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

