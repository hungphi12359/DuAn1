    void FillTable() {
        DefaultTableModel model = (DefaultTableModel) tbl_TTSV.getModel();
        model.setRowCount(0);
        try {
            List<SinhVien> list = svDao.selectSLSV(QuanLyLopHoc.MaLH);
            for (SinhVien sv : list) {
                Object[] row = {
                    sv.getMaSinhVien(),
                    sv.getTenSinhVien(),
                    sv.getEmail(),
                    sv.getSDT(),
                    sv.getDiaChi()
                };
                model.addRow(row);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
    void fixTable() {
        Scrollbar.getViewport().setBackground(Color.WHITE);
        Scrollbar.setVerticalScrollBar(new ScrollBarCustom());
        JPanel p = new JPanel();
        p.setBackground(Color.WHITE);
        Scrollbar.setCorner(JScrollPane.UPPER_RIGHT_CORNER, p);
        Scrollbar.setBorder(new EmptyBorder(5, 10, 5, 10));
    }
    
