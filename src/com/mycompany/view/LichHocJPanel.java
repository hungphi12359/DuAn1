    void loadByMa() {
        try {
            DefaultTableModel model = (DefaultTableModel) tblLichHoc.getModel();
            model.setRowCount(0);
            String MaLopHoc = jctFind.getText();
            List<LichHoc> list = lichhdao.selectByMaLopHoc(MaLopHoc);
            for (LichHoc nv : list) {
                String TenMH =  mhdao.selectById(nv.getMaLopHoc()).getTenMonHoc();
                Object[] row = {
                    nv.getMaLichHoc(),
                    nv.getNgay(),
                    nv.getThoiGian(),
                    nv.getMaMonHoc(),
                    nv.getMaLopHoc(),
                    nv.getMaChuyenNganh(),
                    nv.getMaNhanVien(),
                    nv.getMaPhongHoc(),
                    TenMH,
                     };
                model.addRow(row);

            }
        } catch (Exception e) {
            e.printStackTrace();
            MsgBox.alert(this, "Lỗi truy vấn dữ liệu!");
        }
    }
  
